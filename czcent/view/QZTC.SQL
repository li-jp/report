create or replace view qztc as
select T.USER_ID,
T.USERNAME,
T.JOB_NAME,
T.STATUS,
T.INDUCTION_TIME,
T.STAR_LEVEL,
T.DEPT_ID,
T.DEPTNAME,
T.DAQUNAME,
T.DIMISSION_DATE,
T.MMYJ,
T.ZLYJ,
T.DLYJ,
T.NUM,
PQYJTC_R(T.MMYJ+T.ZLYJ,T.NUM) AS MMYJTC_R,
PQYJTC(T.MMYJ+T.ZLYJ,T.NUM) AS MMYJTC
from QZZYJ t
--WHERE VIEW_DATE.SET_DATE('2018-12') = TO_DATE('2018-12','yyyy-MM');
