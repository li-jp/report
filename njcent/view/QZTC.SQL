create or replace view qztc as
select T.USER_ID,
T.USERNAME,
T.JOB_NAME,
T.STATUS,
T.INDUCTION_TIME,
T.STAR_LEVEL,
T.DEPT_ID,
T.DEPTNAME,
T.DAQUNAME,
T.DIMISSION_DATE,
T.MMYJ,
T.ZLYJ,
T.DLYJ,
(T.MMYJ + T.ZLYJ + T.DLYJ) AS PQZYJ,
PQYJTC_R(T.MMYJ+T.ZLYJ + T.DLYJ) AS YJTC_R,
PQYJTC(T.MMYJ+T.ZLYJ + T.DLYJ) AS YJTC,
T.QZMMYJ,
T.QZZLYJ,
T.QZDLYJ,
(T.QZMMYJ + T.QZZLYJ + T.QZDLYJ) AS QZZYJ,
(T.QZMMYJ + T.QZZLYJ + T.QZDLYJ) * 0.30 AS QZYJTC
from QZZYJ t
--where VIEW_DATE.SET_DATE('2018-11') = TO_DATE('2018-11','yyyy-MM');
