CREATE OR REPLACE FUNCTION DMYJTC(YJ IN NUMBER) RETURN NUMBER IS
  R1     NUMBER;
  RESULT NUMBER;
BEGIN
  SELECT T.RATIO
  INTO   R1
  FROM   JX_RATIO T
  WHERE  T.TYPE = 2
  AND    T.LOWER <= YJ
  AND    T.UPPER > YJ
  AND    T.INPUT_TIME =
         (SELECT MAX(T1.INPUT_TIME)
           FROM   JX_RATIO T1
           WHERE  T1.TYPE = T.TYPE
           AND    T1.LOWER = T.LOWER
           AND    T1.UPPER = T.UPPER
           AND    TO_CHAR(T1.INPUT_TIME, 'yyyy-MM-dd') <=
                  TO_CHAR(LAST_DAY(VIEW_DATE.GET_DATE), 'yyyy-MM-dd'));

  RESULT := YJ * R1;
  RETURN(RESULT);
END DMYJTC;
