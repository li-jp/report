create or replace view dztc as
select T.USER_ID,
T.USERNAME,
T.JOB_NAME,
T.STATUS,
T.INDUCTION_TIME,
T.STAR_LEVEL,
T.DEPT_ID,
T.DEPTNAME,
T.DAQUNAME,
T.DIMISSION_DATE,
T.MMYJ,
T.ZLYJ,
(T.MMYJ + T.ZLYJ) AS DMZYJ,
DMYJTC_R(T.MMYJ+T.ZLYJ) AS MMYJTC_R,
DMYJTC(T.MMYJ+T.ZLYJ) AS MMYJTC,
T.PGF,
PGFTC_R(T.PGF) AS PGFTC_R,
PGFTC(T.PGF) AS PGFTC
from DZZYJ t
--where VIEW_DATE.SET_DATE('2018-10') = TO_DATE('2018-10','yyyy-MM');
