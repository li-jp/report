create or replace view qztc as
select T.USER_ID,
T.USERNAME,
T.JOB_NAME,
T.STATUS,
T.INDUCTION_TIME,
T.STAR_LEVEL,
T.DEPT_ID,
T.DEPTNAME,
T.DAQUNAME,
T.DIMISSION_DATE,
T.NUM,
T.MMYJ,
T.ZLYJ,
T.PGF,
(T.MMYJ + T.ZLYJ + T.PGF) AS PQZYJ,
(T.MMYJ + T.ZLYJ + T.PGF)/T.NUM AS WLDJYJ,--物理店均业绩
PQYJTC_R((T.MMYJ+T.ZLYJ + T.PGF),T.NUM) AS YJTC_R,
PQYJTC((T.MMYJ+T.ZLYJ + T.PGF),T.NUM) AS YJTC

from QZZYJ t
--where VIEW_DATE.SET_DATE('2018-11') = TO_DATE('2018-11','yyyy-MM');
