CREATE OR REPLACE FUNCTION GLTC(GL IN INTEGER, YJ IN NUMBER) RETURN NUMBER IS
  RESULT NUMBER;

BEGIN
  IF GL >= 0
  THEN
    RESULT := YJ * 0.00;
  ELSIF GL >= 1
  THEN
    RESULT := YJ * 0.01;
  ELSIF GL >= 2
  THEN
    RESULT := YJ * 0.02;
  ELSIF GL >= 3
  THEN
    RESULT := YJ * 0.03;
  ELSE
    RESULT := 0;
  END IF;
  RETURN(RESULT);
END GLTC;
