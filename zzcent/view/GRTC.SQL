create or replace view grtc as
select T.USER_ID,
T.USERNAME,
T.JOB_NAME,
T.STATUS,
T.INDUCTION_TIME,
T.STAR_LEVEL,
T.DEPT_ID,
T.DEPTNAME,
T.DAQUNAME,
T.DIMISSION_DATE,
T.GL,
GLTC_R(T.GL) AS GLTC_R,
GLTC(T.MMYJ+T.DLYJ, T.GL) AS GLTC,
T.MMYJ,
MMYJTC_R(T.MMYJ,T.STAR_LEVEL) AS MMYJTC_R,
MMYJTC(T.MMYJ,T.STAR_LEVEL) AS MMYJTC,
T.ZLYJ,
ZLYJTC_R(T.STAR_LEVEL) AS ZLYJTC_R,
ZLYJTC(T.ZLYJ,T.STAR_LEVEL) AS ZLYJTC,
T.DLYJ,
DLYJTC_R(T.STAR_LEVEL) AS DLYJTC_R,
DLYJTC(T.DLYJ,T.STAR_LEVEL) AS DLYJTC
from GRZYJ t;
