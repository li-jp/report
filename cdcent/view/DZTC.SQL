create or replace view dztc as
select T.USER_ID,
T.USERNAME,
T.JOB_NAME,
T.STATUS,
T.INDUCTION_TIME,
T.STAR_LEVEL,
T.DEPT_ID,
T.DEPTNAME,
T.DAQUNAME,
T.DIMISSION_DATE,
T.IF_NEWOROLD,
T.MMYJ,
T.ZLYJ,
DMYJTC_R(T.MMYJ+T.ZLYJ,T.IF_NEWOROLD) AS DMYJTC_R,
DMYJTC(T.MMYJ+T.ZLYJ,T.IF_NEWOROLD) AS DMYJTC,
T.DZMMYJ,
MMYJTC(T.DZMMYJ) AS DZMMYJTC,
MMYJTC_R(T.DZMMYJ) AS DZMMYJTC_R
from DZZYJ t;
