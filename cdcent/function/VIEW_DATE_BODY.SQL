CREATE OR REPLACE PACKAGE BODY VIEW_DATE IS

  PARAM       DATE;
  PARAM_START DATE;
  PARAM_END   DATE;

  FUNCTION SET_DATE(DATE_PARAM VARCHAR2) RETURN DATE IS
  BEGIN
    PARAM := TO_DATE(DATE_PARAM, 'yyyy-MM');
    RETURN PARAM;
  END;

  FUNCTION GET_DATE RETURN DATE IS
  BEGIN
    RETURN PARAM;
  END;

  FUNCTION SET_START_DATE(DATE_PARAM VARCHAR2) RETURN DATE IS
  BEGIN
    PARAM_START := TO_DATE(DATE_PARAM, 'yyyy-MM-dd');
    RETURN PARAM_START;
  END;

  FUNCTION GET_START_DATE RETURN DATE IS
  BEGIN
    RETURN PARAM_START;
  END;

  FUNCTION SET_END_DATE(DATE_PARAM VARCHAR2) RETURN DATE IS
  BEGIN
    PARAM_END := TO_DATE(DATE_PARAM, 'yyyy-MM-dd');
    RETURN PARAM_END;
  END;

  FUNCTION GET_END_DATE RETURN DATE IS
  BEGIN
    RETURN PARAM_END;
  END;

END VIEW_DATE;
